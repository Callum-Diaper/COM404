user_inp = str(input("Towards which direction should I paint? (left, right, up or down) "))

if user_inp == "up":
    print("I am painting in the upward direction!")
elif user_inp == "down":
    print("I am painting in the downwards direction!")
elif user_inp == "left":
    print("I am painting in the leftward direction!")
elif user_inp == "right":
    print("I am painting in the rightward direction!")
else:
    print("I do not know that direction!")

    
